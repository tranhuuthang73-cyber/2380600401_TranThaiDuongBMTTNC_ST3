from flask import Flask, request, jsonify, render_template # Đã thêm render_template ở đây
from cipher.caesar.caesar_cipher import CaesarCipher
from cipher.vigenere.vigenere_cipher import VigenereCipher
from cipher.railfence.railfence_cipher import RailFenceCipher
from cipher.playfair.playfair_cipher import PlayfairCipher
from cipher.transposition.transposition_cipher import TranspositionCipher

app = Flask(__name__)

# Khởi tạo các đối tượng thuật toán
caesar_cipher = CaesarCipher()
vigenere_cipher = VigenereCipher()
railfence_cipher = RailFenceCipher()
playfair_cipher = PlayfairCipher()
transposition_cipher = TranspositionCipher()

# ================= UI WEB TEMPLATES (BÀI 6) =================

# 1. Route cho trang chủ hiển thị menu
@app.route("/")
def home():
    return render_template("index.html")

# 2. Route hiển thị màn hình Caesar và xử lý giao diện
@app.route("/caesar", methods=["GET", "POST"])
def caesar_web():
    result = ""
    if request.method == "POST":
        text = request.form.get("text")
        key = int(request.form.get("key"))
        action = request.form.get("action")
        
        if action == "encrypt":
            result = caesar_cipher.encrypt_text(text, key)
        elif action == "decrypt":
            result = caesar_cipher.decrypt_text(text, key)
            
    return render_template("caesar.html", result=result)


# ================= CÁC ROUTE API CŨ (Giữ lại để hệ thống đầy đủ) =================
@app.route("/api/caesar/encrypt", methods=["POST"])
def caesar_encrypt():
    data = request.json
    plain_text = data['plain_text']
    key = int(data['key'])
    encrypted_text = caesar_cipher.encrypt_text(plain_text, key)
    return jsonify({'encrypted_message': encrypted_text})

@app.route("/api/caesar/decrypt", methods=["POST"])
def caesar_decrypt():
    data = request.json
    cipher_text = data['cipher_text']
    key = int(data['key'])
    decrypted_text = caesar_cipher.decrypt_text(cipher_text, key)
    return jsonify({'decrypted_message': decrypted_text})

